        public void DrawCharacter(char ch, int left, int top, int fgColor, int bgColor) {
            int pxLeft = left * 8;
            int pxTop = top * 16;
            byte[] chPattern = chPatterns[ch];
            // chPattern = ch_41;
            int bmTop = 0;

            for (int b = 0; b < 16; ++b) {
                byte bits = chPattern[b];
                byte mask = 128;

                for (int i = 0; i < 8; ++i) {
                    Color pxColor = Color.Black;

                    if ((bits & mask) == mask) {
                        pxColor = Color.White;
                    }
                    
                    chBitmap.SetPixel(i, bmTop, pxColor);
                    mask >>= 1;
                }

                ++bmTop;
            }

            consoleGraphics.DrawImage(chBitmap, pxLeft, pxTop);
        }

